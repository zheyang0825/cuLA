#!/usr/bin/env python3
"""
generate_benchmark_hopper_md.py — Run Hopper (SM90) benchmarks and generate BENCHMARK_hopper.md

Currently supported Hopper benchmarks:
  - KDA Fused Forward (cula.kda.hopper_fused_fwd)

Reuses bench_kda_fused_fwd.py which auto-selects the correct cuLA implementation
based on GPU architecture (SM90 → hopper_fused_fwd).

Usage:
  python benchmarks/generate_benchmark_hopper_md.py

  # Generate with specific GPU id
  CUDA_VISIBLE_DEVICES=3 python benchmarks/generate_benchmark_hopper_md.py

  # Use cached results (skip re-running benchmarks)
  python benchmarks/generate_benchmark_hopper_md.py --cache tmp/bench_hopper_cache.json
"""

import argparse
import json
import os
import sys
from datetime import datetime
from pathlib import Path

import numpy as np

# Ensure project root is on path
ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

os.environ.setdefault("FLA_USE_FAST_OPS", os.getenv("CULA_USE_FAST_MATH", "1"))  # Enable fast ops in FLA for fair comparison

from benchmarks.bench_kda_fused_fwd import (  # noqa: E402
    _SM_TAG,
)
from benchmarks.bench_kda_fused_fwd import (  # noqa: E402
    D as KDA_FUSED_D,
)
from benchmarks.bench_kda_fused_fwd import (  # noqa: E402
    H as KDA_FUSED_H,
)
from benchmarks.bench_kda_fused_fwd import (  # noqa: E402
    main as kda_fused_fwd_main,
)
from benchmarks.utils import get_env_info  # noqa: E402

BENCHMARK_MD_DEFAULT = "BENCHMARK_H200.md"


# ============================================================
# Run benchmarks
# ============================================================


def run_kda_fused_fwd_benchmarks():
    """Run bench_kda_fused_fwd.main() with programmatic args and return (fixed, varlen) results."""
    print("\n>>> Running KDA Fused Forward benchmarks (via bench_kda_fused_fwd.main)...")
    orig_argv = sys.argv
    sys.argv = ["bench_kda_fused_fwd.py", "--mode", "both"]
    try:
        fixed_res, varlen_res = kda_fused_fwd_main()
    finally:
        sys.argv = orig_argv
    return fixed_res, varlen_res


# ============================================================
# Format markdown
# ============================================================


def format_benchmark_md(env, kda_fused_fixed, kda_fused_varlen):
    lines = []
    w = lines.append

    w("# Benchmark Results — Hopper (SM90)\n")
    w(f"> Auto-generated by `benchmarks/generate_benchmark_hopper_md.py` on {datetime.now().strftime('%Y-%m-%d')}.\n")
    w(f"> **GPU:** {env['gpu']}  |  **CUDA:** {env['cuda']}  |  **PyTorch:** {env['torch']}\n")
    w(
        "> FLA baseline: [flash-linear-attention v0.4.2](https://github.com/fla-org/flash-linear-attention/releases/tag/v0.4.2)\n"
    )
    w("")

    # -------------------------------------------------------------------
    # KDA Fused Forward
    # -------------------------------------------------------------------
    w("\n## KDA Fused Forward (Kimi Delta Attention)\n")
    w(f"Fully-fused KDA forward prefill kernel ({_SM_TAG}).\n")

    # Fixed-length
    if kda_fused_fixed:
        w(f"### Fixed-Length (H={KDA_FUSED_H}, D={KDA_FUSED_D}, bf16)\n")
        w("| B | T | FLA Triton (ms) | cuLA Fused (ms) | Speedup |")
        w("|---|---|-----------------|-----------------|---------|")
        for r in kda_fused_fixed:
            sp = f"**{r['speedup']:.2f}x**"
            w(f"| {r['B']} | {r['T']} | {r['ms_fla']:.3f} | {r['ms_cula']:.3f} | {sp} |")

    # Varlen
    if kda_fused_varlen:
        w(f"\n### Variable-Length (H={KDA_FUSED_H}, D={KDA_FUSED_D}, bf16)\n")
        w("| Config | FLA Triton (ms) | cuLA Fused (ms) | Speedup |")
        w("|--------|-----------------|-----------------|---------|")
        for r in kda_fused_varlen:
            tag = r.get("tag", "unknown")
            sp = f"**{r['speedup']:.2f}x**"
            w(f"| {tag} | {r['ms_fla']:.3f} | {r['ms_cula']:.3f} | {sp} |")

    # Summary stats
    all_results = (kda_fused_fixed or []) + (kda_fused_varlen or [])
    all_sp = [r["speedup"] for r in all_results if r.get("speedup", 0) > 0]
    if all_sp:
        w(
            f"\nSummary ({len(all_sp)} configs): "
            f"**avg={np.mean(all_sp):.2f}x**, min={np.min(all_sp):.2f}x, max={np.max(all_sp):.2f}x.\n"
        )

    w("To reproduce:\n")
    w("```bash")
    w("python benchmarks/bench_kda_fused_fwd.py --mode both")
    w("```\n")

    return "\n".join(lines)


# ============================================================
# Main
# ============================================================


def main():
    parser = argparse.ArgumentParser(description="Generate BENCHMARK_hopper.md")
    parser.add_argument(
        "--cache", type=str, default=None, help="Path to a JSON cache file. If exists, skip benchmarks and use cached results."
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Output markdown filename (relative to project root). Default: BENCHMARK_H200.md",
    )
    parser.add_argument("--save-cache", type=str, default=None, help="Save benchmark results to JSON for future --cache use.")
    args = parser.parse_args()

    env = get_env_info()

    if args.cache and os.path.exists(args.cache):
        print(f"Loading cached results from {args.cache}")
        with open(args.cache) as f:
            data = json.load(f)
        kda_fused_fixed = data["kda_fused_fixed"]
        kda_fused_varlen = data["kda_fused_varlen"]
    else:
        kda_fused_fixed, kda_fused_varlen = run_kda_fused_fwd_benchmarks()

        if args.save_cache:
            cache_path = Path(args.save_cache)
            cache_path.parent.mkdir(parents=True, exist_ok=True)

            def sanitize(results):
                out = []
                for r in results:
                    clean = {}
                    for k, v in r.items():
                        if isinstance(v, (list, tuple)):
                            clean[k] = list(v)
                        elif isinstance(v, float) and (np.isnan(v) or np.isinf(v)):
                            clean[k] = None
                        else:
                            clean[k] = v
                    out.append(clean)
                return out

            with open(cache_path, "w") as f:
                json.dump(
                    {
                        "kda_fused_fixed": sanitize(kda_fused_fixed),
                        "kda_fused_varlen": sanitize(kda_fused_varlen),
                    },
                    f,
                    indent=2,
                )
            print(f"Cached results to {cache_path}")

    md = format_benchmark_md(env, kda_fused_fixed, kda_fused_varlen)

    output_path = ROOT / (args.output if args.output else BENCHMARK_MD_DEFAULT)
    output_path.write_text(md)
    print(f"\nWrote {output_path}")


if __name__ == "__main__":
    main()
