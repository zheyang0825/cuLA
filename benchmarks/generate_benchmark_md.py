"""
generate_benchmark_md.py — Run existing bench_* scripts and generate BENCHMARK.md

Reuses bench_kda.py and bench_lightning_attn.py to collect results,
then formats them as BENCHMARK.md.

Usage:
  python benchmarks/generate_benchmark_md.py

  # Generate with specific GPU id
  CUDA_VISIBLE_DEVICES=3 python benchmarks/generate_benchmark_md.py

  # Use cached results (skip re-running benchmarks)
  python benchmarks/generate_benchmark_md.py --cache tmp/bench_cache.json
"""

import argparse
import json
import os
import sys
from datetime import datetime
from pathlib import Path
from types import SimpleNamespace

# Ensure project root is on path
ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

os.environ.setdefault("FLA_USE_FAST_OPS", os.getenv("CULA_USE_FAST_MATH", "1"))  # Enable fast ops in FLA for fair comparison

import numpy as np  # noqa: E402

from benchmarks.bench_kda import (  # noqa: E402
    D as KDA_D,
)
from benchmarks.bench_kda import (  # noqa: E402
    H as KDA_H,
)
from benchmarks.bench_kda import (  # noqa: E402
    main as kda_main,
)
from benchmarks.bench_la_decode_vs_fla import (  # noqa: E402
    main as la_decode_main,
)
from benchmarks.bench_lightning_attn import (  # noqa: E402
    D_DEFAULT as LA_D,
)
from benchmarks.bench_lightning_attn import (  # noqa: E402
    _valid,
)
from benchmarks.bench_lightning_attn import (  # noqa: E402
    run_benchmark_suite as la_run_suite,
)
from benchmarks.utils import get_env_info  # noqa: E402

BENCHMARK_MD_DEFAULT = "BENCHMARK_GB200.md"

LA_H = 64  # for display only


# ============================================================
# Run benchmarks by calling existing bench scripts
# ============================================================


def run_kda_benchmarks():
    """Run bench_kda.main() with programmatic args and return (fixed, varlen) results."""
    print("\n>>> Running KDA benchmarks (via bench_kda.main)...")
    # bench_kda.main() parses sys.argv — override it temporarily
    orig_argv = sys.argv
    sys.argv = ["bench_kda.py", "--mode", "both"]
    try:
        fixed_res, varlen_res = kda_main()
    finally:
        sys.argv = orig_argv
    return fixed_res, varlen_res


def run_la_decode_benchmarks():
    """Run bench_la_decode_vs_fla benchmarks with programmatic args."""
    print("\n>>> Running LA Decode benchmarks (via bench_la_decode_vs_fla.main)...")
    orig_argv = sys.argv
    sys.argv = [
        "bench_la_decode_vs_fla.py",
        "--batch-sizes",
        "1",
        "4",
        "16",
        "64",
        "256",
        "--heads",
        "64",
        "--head-dim",
        "128",
    ]
    try:
        results = la_decode_main()
    finally:
        sys.argv = orig_argv
    return results


def run_lightning_attn_benchmarks():
    """Run bench_lightning_attn.run_benchmark_suite() with programmatic args."""
    print("\n>>> Running Lightning Attention benchmarks (via bench_lightning_attn)...")
    args = SimpleNamespace(
        modes=["no_state", "varlen"],
        batch_sizes=[1, 2],
        seq_lens=[1024, 4096, 8192, 16384],
        num_heads=[64],
        head_dim=128,
        layer_idx=12,
        num_layers=24,
        warmup=5,
        iterations=20,
        plot=False,
        report=False,
    )
    all_results = la_run_suite(args)
    standard = [r for r in all_results if r["mode"] == "no_state"]
    varlen = [r for r in all_results if r["mode"] == "varlen"]
    return standard, varlen


# ============================================================
# Format markdown
# ============================================================


def format_benchmark_md(env, kda_fixed, kda_varlen, la_standard, la_varlen, la_decode=None):
    lines = []
    w = lines.append

    w("# Benchmark Results\n")
    w(f"> Auto-generated by `benchmarks/generate_benchmark_md.py` on {datetime.now().strftime('%Y-%m-%d')}.\n")
    w(f"> **GPU:** {env['gpu']}  |  **CUDA:** {env['cuda']}  |  **PyTorch:** {env['torch']}\n")
    w(
        "> FLA baseline: [flash-linear-attention v0.4.2](https://github.com/fla-org/flash-linear-attention/releases/tag/v0.4.2)\n"
    )
    w("")

    # -------------------------------------------------------------------
    # KDA
    # -------------------------------------------------------------------
    w("\n## KDA (Kimi Delta Attention)\n")

    # Fixed-length
    w(f"### Fixed-Length (H={KDA_H}, D={KDA_D}, bf16)\n")
    w("| B | T | FLA Triton (ms) | cuLA (ms) | Speedup |")
    w("|---|---|-----------------|-----------|---------|")
    for r in kda_fixed:
        sp = f"**{r['speedup']:.2f}x**"
        w(f"| {r['B']} | {r['T']} | {r['ms_fla']:.3f} | {r['ms_cula']:.3f} | {sp} |")

    kda_fixed_sp = [r["speedup"] for r in kda_fixed if _valid(r.get("speedup", float("nan")))]
    if kda_fixed_sp:
        w(
            f"\nSummary ({len(kda_fixed_sp)} configs): "
            f"**avg={np.mean(kda_fixed_sp):.2f}x**, min={np.min(kda_fixed_sp):.2f}x, max={np.max(kda_fixed_sp):.2f}x.\n"
        )

    # Varlen
    w(f"\n### Variable-Length (H={KDA_H}, D={KDA_D}, bf16)\n")
    w("| Config | FLA Triton (ms) | cuLA (ms) | Speedup |")
    w("|--------|-----------------|-----------|---------|")
    for r in kda_varlen:
        tag = r.get("tag", "unknown")
        sp = f"**{r['speedup']:.2f}x**"
        w(f"| {tag} | {r['ms_fla']:.3f} | {r['ms_cula']:.3f} | {sp} |")

    kda_varlen_sp = [r["speedup"] for r in kda_varlen if _valid(r.get("speedup", float("nan")))]
    if kda_varlen_sp:
        w(
            f"\nSummary ({len(kda_varlen_sp)} configs): "
            f"**avg={np.mean(kda_varlen_sp):.2f}x**, min={np.min(kda_varlen_sp):.2f}x, max={np.max(kda_varlen_sp):.2f}x.\n"
        )

    w("\nTo reproduce:\n")
    w("```bash")
    w("python benchmarks/bench_kda.py --mode both")
    w("```\n")

    # -------------------------------------------------------------------
    # Lightning Attention
    # -------------------------------------------------------------------
    w("## Lightning Attention\n")

    # Standard prefill
    w(f"### Prefill (H={LA_H}, D={LA_D}, bf16)\n")
    w("| B | T | FLA Triton (ms) | cuLA (ms) | Speedup |")
    w("|---|---|-----------------|-----------|---------|")
    for r in la_standard:
        if not _valid(r.get("speedup", float("nan"))):
            continue
        sp = f"**{r['speedup']:.2f}x**"
        w(f"| {r['B']} | {r['T']} | {r['fla_ms']:.3f} | {r['cutedsl_ms']:.3f} | {sp} |")

    # Varlen summary (select representative rows to avoid verbosity)
    w(f"\n### Variable-Length (H={LA_H}, D={LA_D}, bf16)\n")
    w("Persistent CuTe DSL kernel vs FLA Triton varlen.\n")

    # Pick a representative subset: one per (N, T) — use "uniform" dist only for the table
    # but show summary stats across all dists
    representative = [r for r in la_varlen if r.get("dist") == "uniform"]
    w("| N (seqs) | T | cuLA (ms) | FLA Triton (ms) | Speedup |")
    w("|----------|---|-----------|-----------------|---------|")
    for r in representative:
        p_ms = r.get("persistent_ms", float("nan"))
        fla_ms = r.get("fla_varlen_ms", float("nan"))
        sp = r.get("p_vs_fla_vl_speedup", float("nan"))
        if not _valid(p_ms) or not _valid(fla_ms):
            continue
        sp_str = f"**{sp:.2f}x**" if _valid(sp) else "-"
        w(f"| {r['B']} | {r['T']} | {p_ms:.3f} | {fla_ms:.3f} | {sp_str} |")

    # Summary stats across all varlen configs
    all_sp = [r["p_vs_fla_vl_speedup"] for r in la_varlen if _valid(r.get("p_vs_fla_vl_speedup", float("nan")))]
    if all_sp:
        w(
            f"\nSummary ({len(all_sp)} configs across uniform/skewed/random): "
            f"**avg={np.mean(all_sp):.2f}x**, min={np.min(all_sp):.2f}x, max={np.max(all_sp):.2f}x.\n"
        )

    w("To reproduce:\n")
    w("```bash")
    w("python benchmarks/bench_lightning_attn.py --modes no_state varlen")
    w("```\n")

    # -------------------------------------------------------------------
    # Lightning Attention Decode
    # -------------------------------------------------------------------
    if la_decode:
        w("### Decode (H=64, D=128, bf16, T=1)\n")
        w("Single-token decode: la_decode (CuTe DSL) vs fla fused_recurrent (Triton).\n")

        # Kernel-only table
        w("#### Kernel-Only\n")
        w("| B | FLA Triton (ms) | cuLA (ms) | Speedup |")
        w("|---|-----------------|-----------|---------|")
        for r in la_decode:
            sp = f"**{r['kernel_speedup']:.2f}x**"
            w(f"| {r['B']} | {r['kernel_fla_ms']:.4f} | {r['kernel_cute_ms']:.4f} | {sp} |")

        # Wrapper table
        w("\n#### Wrapper (Full Call Path)\n")
        w("| B | FLA Triton (ms) | cuLA (ms) | Speedup |")
        w("|---|-----------------|-----------|---------|")
        for r in la_decode:
            sp = f"**{r['wrap_speedup']:.2f}x**"
            w(f"| {r['B']} | {r['wrap_fla_ms']:.4f} | {r['wrap_cute_ms']:.4f} | {sp} |")

        w("\nTo reproduce:\n")
        w("```bash")
        w("python benchmarks/bench_la_decode_vs_fla.py --heads 64 --head-dim 128")
        w("```\n")

    return "\n".join(lines)


# ============================================================
# Main
# ============================================================


def main():
    parser = argparse.ArgumentParser(description="Generate BENCHMARK.md")
    parser.add_argument(
        "--cache", type=str, default=None, help="Path to a JSON cache file. If exists, skip benchmarks and use cached results."
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Output markdown filename (relative to project root). Default: BENCHMARK_GB200.md",
    )
    parser.add_argument("--save-cache", type=str, default=None, help="Save benchmark results to JSON for future --cache use.")
    args = parser.parse_args()

    env = get_env_info()

    if args.cache and os.path.exists(args.cache):
        print(f"Loading cached results from {args.cache}")
        with open(args.cache) as f:
            data = json.load(f)
        kda_fixed = data["kda_fixed"]
        kda_varlen = data["kda_varlen"]
        la_standard = data["la_standard"]
        la_varlen = data["la_varlen"]
        la_decode = data.get("la_decode", [])
    else:
        kda_fixed, kda_varlen = run_kda_benchmarks()
        la_standard, la_varlen = run_lightning_attn_benchmarks()
        la_decode = run_la_decode_benchmarks()

        if args.save_cache:
            cache_path = Path(args.save_cache)
            cache_path.parent.mkdir(parents=True, exist_ok=True)

            # Convert results to JSON-serializable form
            def sanitize(results):
                out = []
                for r in results:
                    clean = {}
                    for k, v in r.items():
                        if isinstance(v, (list, tuple)):
                            clean[k] = list(v)
                        elif isinstance(v, float) and (np.isnan(v) or np.isinf(v)):
                            clean[k] = None
                        else:
                            clean[k] = v
                    out.append(clean)
                return out

            with open(cache_path, "w") as f:
                json.dump(
                    {
                        "kda_fixed": sanitize(kda_fixed),
                        "kda_varlen": sanitize(kda_varlen),
                        "la_standard": sanitize(la_standard),
                        "la_varlen": sanitize(la_varlen),
                        "la_decode": sanitize(la_decode),
                    },
                    f,
                    indent=2,
                )
            print(f"Cached results to {cache_path}")

    md = format_benchmark_md(env, kda_fixed, kda_varlen, la_standard, la_varlen, la_decode)

    output_path = ROOT / (args.output if args.output else BENCHMARK_MD_DEFAULT)
    output_path.write_text(md)
    print(f"\nWrote {output_path}")


if __name__ == "__main__":
    main()
